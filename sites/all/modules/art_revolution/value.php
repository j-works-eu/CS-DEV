	function art_revolution_select($name, $options, $class=null, $tooltip=null, $change=null){
		$html = "";
		$html .= '<select name="' . $name . '" class="form-select ' . str_replace('hidden', '', $class) . '" '.($change?'data-change="' . $change . '"':'').' data-toggle="tooltip" data-placement="bottom" data-html="true" title="' . t($tooltip) . '">';
